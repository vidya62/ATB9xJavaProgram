package MyPractice;

public class Arithmetic_Operators {
    public static void main(String[] args) {

        int a=14, b=5;
        float d=14.3f, e=3.2f;

        int c= a/b;
        int r=a%b;
        float f1=d%e;

        float f = (float)a/b;

        System.out.println(c);
        System.out.println(r);
        System.out.println(f);
        System.out.println(f1);

    }
}
